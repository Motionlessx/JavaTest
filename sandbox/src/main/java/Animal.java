public class Animal {
    private String say = "Moo-Moo";
    private int teeth = 20;
    private int leg = 5;



    public void setLeg(int leg) {
        this.leg = leg;
    }
    public int getLeg() {
        return leg;
    }

    public String getSay() {
        return say;
    }

    public void setSay(String say) {
        this.say = say;
    }

    public int getTeeth() {
        return teeth;
    }

    public void setTeeth(int teeth) {
        this.teeth = teeth;
    }
}
